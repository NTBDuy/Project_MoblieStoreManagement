    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        if(txtTenKH.getText().isEmpty()){
            MessBox.alert(null, "Bạn chưa nhập họ tên khách hàng!");
        } else {
            try {
                KhachHang kh = getForm();
                if(khBLL.them(kh)){
                    MessBox.alert(null, "Thêm khách hàng thành công!");
                    form_BanHang.txtMaKH.setText(txtMaKH.getText());
                    form_BanHang.txtTenKH.setText(txtTenKH.getText());
                    form_BanHang.txtDiaChi.setText(txtDiaChi.getText());
                    form_BanHang.txtDT.setText(String.valueOf(txtSDT.getText()));
                    this.dispose();
                } else {
                }
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_btnLuuActionPerformed

