    KhachHang getForm() throws ParseException {
        KhachHang kh = new KhachHang();
        kh.setMaKH(txtMaKH.getText());
        kh.setTenKH(txtTenKH.getText());
        boolean gt = true;
        if (!rdoNam.isSelected())
            gt = false;
        kh.setGioiTinh(gt);
        kh.setNgaySinh(dateChs.getDate());
        kh.setCMND(txtCMND.getText());
        kh.setSoDT(txtSDT.getText());
        kh.setDiaChi(txtDiaChi.getText());
        kh.setEmail(txtEmail.getText());
        kh.setGhiChu(txtGhiChu.getText());
        return kh;
    }
    public void Load(){
        this.setLocationRelativeTo(null);
        try {
            txtMaKH.setText(khBLL.autoID().trim());
        } catch (SQLException ex) {
            Logger.getLogger(form_NhapKHang.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
